/* The 'constructor' of a dynarray object: initializes all the
 * variables to well-defined startup values */

	/* Prototypes */
	public static void init_dynarray(dynarray this_Renamed, int entry_size, int size, int increment)
	{
		this_Renamed.v = 0; // preset value, in case gp_alloc fails
		if (size != 0)
		this_Renamed.v = GlobalMembersAlloc.gp_alloc(entry_size * size, "init dynarray");
		this_Renamed.size = size;
		this_Renamed.end = 0;
		this_Renamed.increment = increment;
		this_Renamed.entry_size = entry_size;
	}

/* The 'destructor'; sets all crucial elements of the structure to
 * well-defined values to avoid problems by use of bad pointers... */
	public static void free_dynarray(dynarray this_Renamed)
	{
//C++ TO JAVA CONVERTER TODO TASK: The memory management function 'free' has no equivalent in Java:
		free(this_Renamed.v); // should work, even if gp_alloc failed
		this_Renamed.v = 0;
		this_Renamed.end = this_Renamed.size = 0;
	}

/* Increase the size of the dynarray by a given amount */
	public static void extend_dynarray(dynarray this_Renamed, int increment)
	{
		GlobalMembersDynarray.resize_dynarray(this_Renamed, this_Renamed.size + increment);
	}

/* Set the size of the dynamical array to a new, fixed value */
	public static void resize_dynarray(dynarray this_Renamed, int newsize)
	{
		if (this_Renamed.v == null)
		GlobalMembersUtil.graph_error("resize_dynarray: dynarray wasn't initialized!");

		if (newsize == 0)
		GlobalMembersDynarray.free_dynarray(this_Renamed);
		else
		{
		this_Renamed.v = GlobalMembersAlloc.gp_realloc(this_Renamed.v, this_Renamed.entry_size * newsize, "extend dynarray");
		this_Renamed.size = newsize;
		}
	}

/* Get pointer to the element one past the current end of the dynamic
 * array. Resize it if necessary. Returns a pointer-to-void to that
 * element. */
	public static void nextfrom_dynarray(dynarray this_Renamed)
	{
		if (this_Renamed.v == null)
		GlobalMembersUtil.graph_error("nextfrom_dynarray: dynarray wan't initialized!");

		if (this_Renamed.end >= this_Renamed.size)
		GlobalMembersDynarray.extend_dynarray(this_Renamed, this_Renamed.increment);
		return (Object)((String)(this_Renamed.v) + this_Renamed.entry_size * (this_Renamed.end++));
	}

/* Release the element at the current end of the dynamic array, by
 * moving the 'end' index one element backwards */
	public static void droplast_dynarray(dynarray this_Renamed)
	{
		if (this_Renamed.v == null)
		GlobalMembersUtil.graph_error("droplast_dynarray: dynarray wasn't initialized!");

		if (this_Renamed.end != 0)
		this_Renamed.end--;
	}
